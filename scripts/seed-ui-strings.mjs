#!/usr/bin/env node
// seed-ui-strings.mjs
//
// Generates idempotent SQL that loads the mobile app's bundled UI texts
// (locales/en.json + locales/ka.json) into the public.ui_strings table that
// backs the text CMS (see supabase/migrations/20260622120000_ui_strings.sql).
//
// It emits SQL (rather than writing directly) so it runs with zero npm deps and
// no service-role key in your shell — apply it with the already-authed CLI:
//
//   node scripts/seed-ui-strings.mjs                 # insert-only, preserves CMS edits
//   supabase db query --linked --yes --file supabase/seed-ui-strings.generated.sql
//
// Modes:
//   (default)  ON CONFLICT DO NOTHING  — registers NEW keys, never overwrites
//              rows a co-worker already edited. This is the drift-management tool:
//              run it after adding keys to locales/*.json so they appear in the CMS.
//   --reset    ON CONFLICT DO UPDATE   — force the JSON values back into the table
//              (discards CMS edits). Use sparingly.
//
// Options:
//   --out <path>      where to write the SQL (default supabase/seed-ui-strings.generated.sql)
//   --prefix <prefix>  only emit keys starting with this dotted prefix (e.g. "inspections.").
//                       Combine with --reset to generate a scoped, machine-generated upsert for
//                       a specific migration/fix instead of hand-writing SQL rows — the upsert
//                       only ever touches keys under that prefix, leaving every other row alone.

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, '..');

const args = process.argv.slice(2);
const reset = args.includes('--reset');
const outIdx = args.indexOf('--out');
const outPath = outIdx >= 0 ? resolve(args[outIdx + 1]) : join(repoRoot, 'supabase', 'seed-ui-strings.generated.sql');
const prefixIdx = args.indexOf('--prefix');
const prefix = prefixIdx >= 0 ? args[prefixIdx + 1] : '';

/**
 * Flatten a nested i18n object to dotted paths. Arrays use numeric segments
 * (calendar.monthLabels.0). Only STRING leaves are emitted — the CMS edits text,
 * so numbers/booleans/null stay bundled-only and are not overridable. This MUST
 * match the unflatten() convention in lib/i18nOverlay.ts.
 */
function flatten(obj, prefix, out) {
  for (const [k, v] of Object.entries(obj)) {
    const key = prefix ? `${prefix}.${k}` : k;
    if (v !== null && typeof v === 'object') {
      flatten(v, key, out);
    } else if (typeof v === 'string') {
      out[key] = v;
    }
  }
  return out;
}

function load(lang) {
  const path = join(repoRoot, 'locales', `${lang}.json`);
  return flatten(JSON.parse(readFileSync(path, 'utf8')), '', {});
}

const en = load('en');
const ka = load('ka');

// Union of keys across both languages (a key may exist in one and not the other),
// optionally scoped to --prefix.
const allKeys = [...new Set([...Object.keys(en), ...Object.keys(ka)])].sort();
const keys = prefix ? allKeys.filter((k) => k.startsWith(prefix)) : allKeys;

if (prefix && keys.length === 0) {
  console.error(`No keys found matching prefix "${prefix}" in locales/en.json / ka.json.`);
  process.exit(1);
}

const sqlStr = (v) => (v == null ? 'NULL' : `'${String(v).replace(/'/g, "''")}'`);

const values = keys.map((k) => `  (${sqlStr(k)}, ${sqlStr(en[k])}, ${sqlStr(ka[k])})`).join(',\n');

const onConflict = reset
  ? `on conflict (key) do update set en = excluded.en, ka = excluded.ka, updated_at = now()`
  : `on conflict (key) do nothing`;

const sql = `-- Generated by scripts/seed-ui-strings.mjs ${reset ? '(--reset)' : '(insert-only)'}${prefix ? ` --prefix ${prefix}` : ''}
-- ${keys.length} keys from locales/en.json + locales/ka.json. Do not edit by hand.
insert into public.ui_strings (key, en, ka) values
${values}
${onConflict};
`;

writeFileSync(outPath, sql, 'utf8');
console.log(`Wrote ${keys.length} keys → ${outPath}`);
console.log(`Apply with:\n  supabase db query --linked --yes --file ${outPath}`);
